//! Definitions for all output payloads returned by hooks.

use crate::types::Decision;

use serde::Serialize;
use serde_json::Value;

/// Represents the overall output from any hook.
///
/// This enum is untagged so it serializes directly into the underlying variant's structure.
#[derive(Debug, Serialize, strum_macros::AsRefStr, strum_macros::IntoStaticStr)]
#[serde(untagged)]
pub enum HookOutputEvent {
    /// Output for a `BeforeTool` event.
    BeforeTool(BeforeToolOutput),
    /// Output for an `AfterTool` event.
    AfterTool(AfterToolOutput),
    /// Output for a `BeforeAgent` event.
    BeforeAgent(BeforeAgentOutput),
    /// Output for an `AfterAgent` event.
    AfterAgent(AfterAgentOutput),
    /// Output for a `BeforeModel` event.
    BeforeModel(BeforeModelOutput),
    /// Output for an `AfterModel` event.
    AfterModel(AfterModelOutput),
    /// Output for a `BeforeToolSelection` event.
    BeforeToolSelection(BeforeToolSelectionOutput),
    /// Output for a `SessionStart` event.
    SessionStart(SessionStartOutput),
    /// Output for a `SessionEnd` event.
    SessionEnd(SessionEndOutput),
    /// Output for a `Notification` event.
    Notification(NotificationOutput),
    /// Output for a `PreCompress` event.
    PreCompress(PreCompressOutput),
    /// Output for a `CwdChanged` event.
    CwdChanged(CwdChangedOutput),
    /// Output for a `FileChanged` event.
    FileChanged(FileChangedOutput),
    /// Output for an `InstructionsLoaded` event.
    InstructionsLoaded(InstructionsLoadedOutput),
    /// Output for a `UserPromptSubmit` event.
    UserPromptSubmit(UserPromptSubmitOutput),
    /// Output for a `WorktreeCreate` event.
    WorktreeCreate(WorktreeCreateOutput),
    /// Output for a `WorktreeRemove` event.
    WorktreeRemove(EmptyOutput),

    // --- Additional Hooks ---
    /// Output for a `Setup` event. Fires only on explicit setup/initialization.
    Setup(EmptyOutput),
    /// Output for a `UserPromptExpansion` event. Runs when a slash command expands.
    UserPromptExpansion(EmptyOutput),
    /// Output for a `MessageDisplay` event. Runs when a message incrementally displays.
    MessageDisplay(EmptyOutput),
    /// Output for a `PermissionRequest` event. Runs when a permission dialog is shown.
    PermissionRequest(EmptyOutput),
    /// Output for a `PostToolUseFailure` event. Runs when a tool throws an error or fails.
    PostToolUseFailure(EmptyOutput),
    /// Output for a `PostToolBatch` event. Runs once after a batch of tools resolves.
    PostToolBatch(EmptyOutput),
    /// Output for a `PermissionDenied` event. Runs when the auto classifier denies a tool.
    PermissionDenied(EmptyOutput),
    /// Output for a `SubagentStart` event. Runs when a subagent is spawned.
    SubagentStart(EmptyOutput),
    /// Output for a `SubagentStop` event. Runs when a subagent finishes.
    SubagentStop(EmptyOutput),
    /// Output for a `TaskCreated` event. Runs when a background task is created.
    TaskCreated(EmptyOutput),
    /// Output for a `TaskCompleted` event. Runs when a task is marked as completed.
    TaskCompleted(EmptyOutput),
    /// Output for a `Stop` event. Runs when the main agent finishes responding.
    Stop(EmptyOutput),
    /// Output for a `StopFailure` event. Runs when a turn ends due to an API error.
    StopFailure(EmptyOutput),
    /// Output for a `TeammateIdle` event. Runs when a teammate is about to go idle.
    TeammateIdle(EmptyOutput),
    /// Output for a `ConfigChange` event. Runs when a configuration file changes.
    ConfigChange(EmptyOutput),
    /// Output for a `PostCompact` event. Runs after a compact operation finishes.
    PostCompact(EmptyOutput),
    /// Output for a `Elicitation` event. Runs when an MCP server requests user input.
    Elicitation(EmptyOutput),
    /// Output for a `ElicitationResult` event. Runs when an elicitation result is received.
    ElicitationResult(EmptyOutput),
}

/// The response generated by a `BeforeTool` hook.
///
/// Returns decisions to allow, block, or modify the execution of a tool.
#[derive(Debug, Default, Serialize)]
pub struct BeforeToolOutput {
    /// The hook's decision on whether to allow the tool execution.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub decision: Option<Decision>,
    /// The reason for the decision, if any.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub reason: Option<String>,
    /// Overridden input arguments for the tool.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub updated_input: Option<Value>,
    /// Additional context injected into the prompt.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub additional_context: Option<String>,
    /// Indicates whether to halt the process entirely.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub halt: Option<bool>,
    /// An exit code to return if halting.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub exit_code: Option<i32>,
}

/// The response generated by an `AfterTool` hook.
///
/// Returns decisions on how to handle the tool's result, optionally suppressing or updating the output.
#[derive(Debug, Default, Serialize)]
pub struct AfterToolOutput {
    /// The hook's decision based on the tool's output.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub decision: Option<Decision>,
    /// The reason for the decision, if any.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub reason: Option<String>,
    /// Additional context injected into the prompt.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub additional_context: Option<String>,
    /// Overridden output returned to the model.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub updated_tool_output: Option<Value>,
    /// If true, suppresses the tool's actual output from the model.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub suppress_output: Option<bool>,
    /// Indicates whether to halt the process entirely.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub halt: Option<bool>,
    /// An exit code to return if halting.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub exit_code: Option<i32>,
}

