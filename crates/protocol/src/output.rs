    #[case(Some(42), Some("Access Denied".to_string()))]
    #[case(None, None)]
    #[case(Some(1), None)]
    fn test_exit_metadata_extraction(
        block_decision: Decision,
        #[case] code: Option<i32>,
        #[case] reason: Option<String>,
    ) -> Result<(), TestError> {
        let output = HookOutputEvent::BeforeTool(BeforeToolOutput {
            decision: Some(block_decision),
            reason: reason.clone(),
            exit_code: code,
            ..Default::default()
        });

        let (extracted_code, extracted_reason) = output.exit_metadata();

        assert_eq!(extracted_code, code);
        assert_eq!(extracted_reason, reason.as_deref());

        Ok(())
    }

